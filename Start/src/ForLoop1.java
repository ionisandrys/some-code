
public class ForLoop1 {

	public static void main(String[] args) {

		for(int i=1; i<=10; i++)
			System.out.println(i+1);

	
	
	for(int j=1; j<5; j++) {
		
		for(int k=1; k<5; k++) {
			
			System.out.println(j + " "+ k);
		}
	}
	
	for(int a = 1; a<=5; a++) {
		for(int b=1; b<=5; b++) {
			System.out.println("* ");
		}
	}

}
}